package variables;

public class CharDataTypeStudy {

	public static void main(String[] args) {
		
		
		// 1. Variable Declaration
		char Division1;
		char Division2;
		char Gender1;
		char Gender2;
		
		// 2. Variable Initialization
		Division1 ='A';
		Division2='C'         ;
		Gender1='M';
		Gender2='F';
		
		// 3. Usage
		System.out.println("The Division 1 is "+Division1);
		System.out.println("The Division 2 is "+Division2);
		System.out.println("Gender is "+Gender1);
		System.out.println("Gender is "+Gender2);

	}

}
